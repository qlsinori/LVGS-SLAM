import numpy as np
import argparse
import sys
from scipy.spatial.transform import Rotation as R
from scipy.spatial.transform import Slerp
from scipy.interpolate import interp1d


def load_data(cam_ts_path, gt_traj_path):
    """????????????????????"""
    print(f"[Info] ??????????????????: {cam_ts_path}")
    try:
        cam_ts = np.loadtxt(cam_ts_path)
        if cam_ts.ndim == 0: cam_ts = np.array([cam_ts])
    except Exception as e:
        print(f"[Error] ??????????????????: {e}")
        sys.exit(1)

    print(f"[Info] ????????????????: {gt_traj_path}")
    try:
        gt_data = np.loadtxt(gt_traj_path)
    except Exception as e:
        print(f"[Error] ????????????????: {e}")
        sys.exit(1)

    # ????????????
    if gt_data.ndim == 1: gt_data = gt_data.reshape(1, -1)
    if gt_data.shape[1] != 13:
        print(f"[Error] ??????????????????????????13?? (Time + 12 elements)???????? {gt_data.shape[1]} ????")
        sys.exit(1)

    # ??????????????????????????
    gt_ts = gt_data[:, 0]
    gt_poses_flat = gt_data[:, 1:]  # (N, 12)

    # ?????????????????????? (Slerp??????)
    sort_idx = np.argsort(gt_ts)
    gt_ts = gt_ts[sort_idx]
    gt_poses_flat = gt_poses_flat[sort_idx]

    # ????????????????
    unique_idx = np.unique(gt_ts, return_index=True)[1]
    gt_ts = gt_ts[unique_idx]
    gt_poses_flat = gt_poses_flat[unique_idx]

    cam_ts = cam_ts[:]
    cam_ts = cam_ts[::4]
    return cam_ts, gt_ts, gt_poses_flat


def process_interpolation(cam_ts, gt_ts, gt_poses_flat):
    """????????????????"""

    # 1. ????????????????
    # ???????????????? [min_t, max_t] ??????????
    t_min, t_max = gt_ts[0], gt_ts[-1]
    valid_mask = (cam_ts >= t_min) & (cam_ts <= t_max)

    target_ts = cam_ts[valid_mask]
    ignored_count = len(cam_ts) - len(target_ts)

    if len(target_ts) == 0:
        print("[Error] ??????????????????????????????????????????????????????")
        sys.exit(1)

    if ignored_count > 0:
        print(f"[Warning] ?????? {ignored_count} ????????????????????????????????")

    print(f"[Info] ?????? {len(target_ts)} ????????????...")

    # 2. ???????? (N, 12) ?????? (N, 3, 4) ????
    # ??????????: r11 r12 r13 tx r21 r22 r23 ty r31 r32 r33 tz
    gt_poses_mat = gt_poses_flat.reshape(-1, 3, 4)

    gt_rot_mats = gt_poses_mat[:, :, :3]  # (N, 3, 3)
    gt_trans = gt_poses_mat[:, :, 3]  # (N, 3)

    # 3. ???????? (T) - ????????
    interp_func_t = interp1d(gt_ts, gt_trans, axis=0, kind='linear')
    out_trans = interp_func_t(target_ts)

    # 4. ???????? (R) - Slerp
    key_rots = R.from_matrix(gt_rot_mats)
    slerp = Slerp(gt_ts, key_rots)
    out_rots = slerp(target_ts)
    out_rot_mats = out_rots.as_matrix()

    # 5. ????????
    num_samples = len(target_ts)
    out_data = np.zeros((num_samples, 13))

    out_data[:, 0] = target_ts  # ??????????????

    # ???? R ?? t -> 3x4
    combined_poses = np.zeros((num_samples, 3, 4))
    combined_poses[:, :, :3] = out_rot_mats
    combined_poses[:, :, 3] = out_trans

    # ??????12????????????
    out_data[:, 1:] = combined_poses.reshape(num_samples, 12)

    return out_data


def main():
    parser = argparse.ArgumentParser(description="?????????????????????????????????????? KITTI ????")

    parser.add_argument("cam_file", type=str, help="????????????????txt????????")
    parser.add_argument("gt_file", type=str, help="??????????????txt???????? (????: time + 12 elements)")
    parser.add_argument("--output", "-o", type=str, default="interpolated_kitti.txt",
                        help="?????????????????????? (KITTI????, ????: interpolated_kitti.txt)")

    args = parser.parse_args()

    # 1. ????
    cam_ts, gt_ts, gt_poses = load_data(args.cam_file, args.gt_file)

    # 2. ????
    result = process_interpolation(cam_ts, gt_ts, gt_poses)

    # 3. ?????? KITTI ????
    # KITTI ???????????????????????? 12 ?? pose ????
    # result ???????? [timestamp, r11, r12, r13, tx, ...]
    kitti_poses = result[:, 1:]
    kitti_poses = kitti_poses[0]

    print("[Info] 正在执行轨迹归一化 (所有帧左乘第一帧的逆)...")

    N = kitti_poses.shape[0]

    # 1. 还原成 (N, 4, 4) 矩阵以便计算
    poses_4x4 = np.eye(4).reshape(1, 4, 4).repeat(N, axis=0)  # 初始化 N 个单位阵
    poses_4x4[:, :3, :] = kitti_poses.reshape(N, 3, 4)  # 填入旋转和平移

    # 2. 计算第一帧的逆矩阵 T0_inv
    T0_inv = np.linalg.inv(poses_4x4[0])

    # 3. 批量矩阵乘法: Normalized = T0_inv @ Poses
    # 利用 numpy 的广播机制: (1,4,4) @ (N,4,4) -> (N,4,4)
    normalized_4x4 = T0_inv[np.newaxis, :, :] @ poses_4x4

    # 4. 变回 KITTI 的 flat 格式 (N, 12)
    kitti_poses = normalized_4x4[:, :3, :].reshape(N, 12)

    # ?????????????????????????????????????? KITTI ???????????????? (????: 1.000000e+00)
    np.savetxt(args.output, kitti_poses, fmt='%.6e', delimiter=' ')
    np.savetxt(args.output+"%4%5", kitti_poses[::5,:], fmt='%.6e', delimiter=' ')
    print(f"[Success] ??????????KITTI ????????????????: {args.output}")


if __name__ == "__main__":
    main()